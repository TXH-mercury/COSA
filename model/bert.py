    def update_attention_mask(self, attention_mask):
        b,n,n = attention_mask.shape
        #attention_mask = torch.cat([attention_mask, attention_mask.new_zeros((attention_mask.shape[0], 1))], dim=-1)
        attention_mask_update = attention_mask.new_zeros(b,n+1,n+1)
        attention_mask_update[:,:n,:n] = attention_mask
        attention_mask_update[:,n, :n] = attention_mask[:, n-1,:n]
        attention_mask_update[:,n, n] = 1
        return attention_mask_update
    
    def update_position_ids(self, position_ids):
        b,n = position_ids.shape
        position_ids_update = position_ids.new_zeros(b,n+1)
        position_ids_update[:,:n] = position_ids
        position_ids_update[:,n] = position_ids_update[:,n-1]+1
        return position_ids_update

    def  prepare_inputs_for_generation(self, input_ids, attention_mask=None, position_ids=None, encoder_hidden_states=None, **model_kwargs):
        input_shape = input_ids.shape
        effective_batch_size = input_shape[0]

        #  add a dummy token
        # if self.config.pad_token_id is None:
        #     raise ValueError("The PAD token should be defined for generation")
        attention_mask = self.update_attention_mask(attention_mask)
        dummy_token = torch.full(
            (effective_batch_size, 1), self.tokenizer.mask_token_id, dtype=torch.long, device=input_ids.device
        )
        input_ids = torch.cat([input_ids, dummy_token], dim=1)


        if position_ids is not None:
            position_ids = self.update_position_ids(position_ids)

        return {"input_ids": input_ids, "attention_mask": attention_mask,'position_ids':position_ids, 'encoder_hidden_states':encoder_hidden_states}


    def _update_model_kwargs_for_generation(
        self,
        outputs: ModelOutput,
        model_kwargs: Dict[str, Any],
        is_encoder_decoder: bool = False,
        standardize_cache_format: bool = False,
    ) -> Dict[str, Any]:
        # update past_key_values
        model_kwargs["past_key_values"] = self._extract_past_from_model_output(
            outputs, standardize_cache_format=standardize_cache_format
        )

        # update token_type_ids with last value
        if "token_type_ids" in model_kwargs:
            token_type_ids = model_kwargs["token_type_ids"]
            model_kwargs["token_type_ids"] = torch.cat([token_type_ids, token_type_ids[:, -1].unsqueeze(-1)], dim=-1)


        if "position_ids" in model_kwargs:
                position_ids = model_kwargs["position_ids"]
                # model_kwargs["attention_mask"] = torch.cat(
                #     [attention_mask, attention_mask.new_ones((attention_mask.shape[0], 1))], dim=-1
                # )
                model_kwargs["position_ids"] = self.update_position_ids(position_ids)

        if not is_encoder_decoder:
            # update attention mask

            if "attention_mask" in model_kwargs:
                attention_mask = model_kwargs["attention_mask"]
                # model_kwargs["attention_mask"] = torch.cat(
                #     [attention_mask, attention_mask.new_ones((attention_mask.shape[0], 1))], dim=-1
                # )
                model_kwargs["attention_mask"] = self.update_attention_mask(attention_mask)
        else:
            # update decoder attention mask
            if "decoder_attention_mask" in model_kwargs:
                decoder_attention_mask = model_kwargs["decoder_attention_mask"]
                model_kwargs["decoder_attention_mask"] = torch.cat(
                    [decoder_attention_mask, decoder_attention_mask.new_ones((decoder_attention_mask.shape[0], 1))],
                    dim=-1,
                )

        return model_kwargs

    def _expand_inputs_for_generation(
        *args,
        expand_size: int = 1,
        is_encoder_decoder: bool = False,
        input_ids: Optional[torch.LongTensor] = None,
        **model_kwargs,
        ) -> Tuple[torch.LongTensor, Dict[str, Any]]:
        """Expands tensors from [batch_size, ...] to [batch_size * expand_size, ...]"""

        if input_ids is not None:
            input_ids = input_ids.repeat_interleave(expand_size, dim=0)

        if model_kwargs.get("token_type_ids") is not None:
            model_kwargs["token_type_ids"] = model_kwargs["token_type_ids"].repeat_interleave(expand_size, dim=0)

        if model_kwargs.get("attention_mask") is not None:
            model_kwargs["attention_mask"] = model_kwargs["attention_mask"].repeat_interleave(expand_size, dim=0)
        
        if model_kwargs.get("position_ids") is not None:
            model_kwargs["position_ids"] = model_kwargs["position_ids"].repeat_interleave(expand_size, dim=0)

        if model_kwargs.get("encoder_hidden_states") is not None:
            model_kwargs["encoder_hidden_states"] = model_kwargs["encoder_hidden_states"].repeat_interleave(expand_size, dim=0)

        if is_encoder_decoder:
            encoder_outputs = model_kwargs.get("encoder_outputs")
            if encoder_outputs is None:
                raise ValueError("If `is_encoder_decoder` is True, make sure that `encoder_outputs` is defined.")
            encoder_outputs["last_hidden_state"] = encoder_outputs.last_hidden_state.repeat_interleave(
                expand_size, dim=0
            )
            model_kwargs["encoder_outputs"] = encoder_outputs
            decoder_attention_mask = model_kwargs.get("decoder_attention_mask")
            if decoder_attention_mask is not None:
                model_kwargs["decoder_attention_mask"] = decoder_attention_mask.repeat_interleave(expand_size, dim=0)

        return input_ids, model_kwargs


